package com.acuilab.bc.cfx.solidity;

/**
 *
 * @author acuilab.com
 */
public class JavaClassGeneratorException extends Exception {

    public JavaClassGeneratorException() {
    }

    public JavaClassGeneratorException(String message) {
        super(message);
    }

    public JavaClassGeneratorException(String message, Throwable cause) {
        super(message, cause);
    }

    public JavaClassGeneratorException(Throwable cause) {
        super(cause);
    }

    public JavaClassGeneratorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
